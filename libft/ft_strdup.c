#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*ptr;
	int		size;
	int		i;

	i = 0;
	size = ft_strlen(src);
	ptr = malloc(size + 1);
	if (!ptr)
		return (NULL);
	while (src[i])
	{
		ptr[i] = src[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
